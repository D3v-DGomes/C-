/*

Operador    Aritmético	        Descrição

+	        Adição
-	        Subtração
*	        Multiplicação
/	        Divisão
%	        Módulo              (resto da divisão)

*/


/*

Operador        Aritmético	        Descrição
+=	            mais igual
-=	            menos igual
*=	            vezes igual
/=	            dividido igual
%=	            módulo igual

*/

/*

Operador        Relacional	                    Descrição
==	            Igual a                         x == y será False
!=	            Diferente de                    x != y será False
>	            Maior que                       x > y será False
<	            Menor que   que x > y ser       x < y será True
>=	            Maior do que ou igual a         X >= y será False
<=	            Menor do que ou igual a         x <= y será True

*/


/*

Operador Lógico	        Descrição
&&	                    AND = E --> o resultado somente será verdadeiro (true) se todas as expressões lógicas forem avaliadas como verdadeiras.
||	                    OR = Ou --> basta que apenas uma das expressões lógicas seja verdadeira para que o resultado também seja verdadeiro.
!	                    NOT = Não --> é usado para gerar uma negação. Desta forma, é invertida toda a lógica da expressão.

*/
